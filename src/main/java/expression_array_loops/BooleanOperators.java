package expression_array_loops;

public class BooleanOperators {
    public static void main(String[] aa){
        int i=5;
        System.out.println("i: "+i);

        boolean b= (i==5);
        System.out.println("(i==5) : "+b);

        b= (i!=5);
        System.out.println("(i!=5) : "+b);

        b= (i<5);
        System.out.println("(i<5) : "+b);

        b= (i<=5);
        System.out.println("(i<=5) : "+b);

        b= (i>5);
        System.out.println("(i>5) : "+b);

        b= (i>=5);
        System.out.println("(i>=5) : "+b);
    }
}
